public class Cane {

    private String nome;
    private String razza;

    public Cane(String nome, String razza){
        this.nome = nome;
        this.razza = razza;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getRazza() {
        return razza;
    }

    public void setRazza(String razza) {
        this.razza = razza;
    }

}
